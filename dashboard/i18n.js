  const res = () => RES[lang] || null;

  function compactNum(v) {                 // 12,345,678 → "12.35M"
    if (!isFinite(v)) return String(v);
    const a = Math.abs(v), t = x => x.toFixed(2).replace(/\.?0+$/, '');
    if (a >= 1e6) return t(v / 1e6) + 'M';
    if (a >= 1e3) return t(v / 1e3) + 'K';
    return String(Math.round(v));
  }
  function convertNums(s) {                 // "1,798만" → "17.98M", "1.7억" → "170M"
    return s
      .replace(/(\d[\d,]*(?:\.\d+)?)\s*억/g, (_m, n) => compactNum(parseFloat(n.replace(/,/g, '')) * 1e8))
      .replace(/(\d[\d,]*(?:\.\d+)?)\s*만/g, (_m, n) => compactNum(parseFloat(n.replace(/,/g, '')) * 1e4));
  }

  function buildSub(R) {
    const subEntries = Object.entries(R.SUB);
    // 이름 전부 포함(1글자 포함 — 로그 임베드 대응). 데이터가 출처(란=Souran·멍=Wang).
    // 오프라인 감사로 훼손 0 확인됨. 1글자 이름이 다른 한국어 단어에 박히면 _audit 로 잡을 것.
    R._sub = [...subEntries, ...R.names].sort((a, b) => b[0].length - a[0].length);
  }

  function tName(kr) {                       // PAT 콜백용 — 이름/더미N 등 단건 치환
    const R = res(); if (!R) return kr;
    const k = (kr || '').trim();
    if (R.EXACT[k] != null) return R.EXACT[k];
    if (!HANGUL.test(kr)) return kr;
    let out = kr;                            // REGEX(더미N 등) + SUB 부분치환
    if (R.REGEX) for (const [re, rep] of R.REGEX) out = out.replace(re, rep);
    if (!R._sub) buildSub(R);
    for (const [a, b] of R._sub) { if (out.indexOf(a) !== -1) { out = out.split(a).join(b); if (!HANGUL.test(out)) break; } }
    return out;
  }

  function translateString(s) {
    const R = res(); if (!R || !HANGUL.test(s)) return null;
    const key = s.trim();
    if (R.EXACT[key] != null) return s.replace(key, R.EXACT[key]);   // 1) 노드 전체 일치
    for (const [re, fn] of R.PAT) {                                  // 2) 패턴(앵커)
      const m = key.match(re);
      if (m) return s.replace(key, fn.apply(null, m));
    }
    let out = s;
    if (R.REGEX) for (const [re, rep] of R.REGEX) out = out.replace(re, rep);   // 3) 정규식 치환
    if (!R._sub) buildSub(R);                                        // 4) 안전한 부분치환
    for (const [kr, en] of R._sub) {
      if (out.indexOf(kr) !== -1) { out = out.split(kr).join(en); if (!HANGUL.test(out)) break; }
    }
    if (R.num) out = convertNums(out);                               // 5) 수 단위 만/억 → K/M
    return out !== s ? out : null;
  }

  const orig = new WeakMap();
  const origAttr = new WeakMap();
  const SKIP = new Set(['SCRIPT', 'STYLE', 'NOSCRIPT']);

  function applyTextNode(node) {
    const cur = orig.has(node) ? orig.get(node) : node.nodeValue;
    const t = translateString(cur);
    if (t != null && t !== node.nodeValue) {
      if (!orig.has(node)) orig.set(node, node.nodeValue);
      node.nodeValue = t;
    }
  }

  function applyAttrs(el) {
    const R = res(); if (!R) return;
    for (const attr of ['title', 'placeholder']) {
      if (!el.hasAttribute(attr)) continue;
      const cur = (origAttr.get(el) && origAttr.get(el)[attr] != null) ? origAttr.get(el)[attr] : el.getAttribute(attr);
      if (!HANGUL.test(cur || '')) continue;
      const t = translateString(cur);
      if (t != null && el.getAttribute(attr) !== t) {
        const store = origAttr.get(el) || {};
        if (store[attr] == null) { store[attr] = el.getAttribute(attr); origAttr.set(el, store); }
        el.setAttribute(attr, t);
      }
    }
  }

  function walk(root) {
    if (root.nodeType === Node.TEXT_NODE) { applyTextNode(root); return; }
    if (root.nodeType !== Node.ELEMENT_NODE) return;
    if (SKIP.has(root.tagName) || root.classList.contains('i18n-skip') || root.closest('.i18n-skip')) return;
    applyAttrs(root);
    const tw = document.createTreeWalker(root, NodeFilter.SHOW_TEXT, {
      acceptNode(n) {
        const p = n.parentElement;
        if (!p || SKIP.has(p.tagName) || p.closest('.i18n-skip')) return NodeFilter.FILTER_REJECT;
        return n.nodeValue && HANGUL.test(n.nodeValue) ? NodeFilter.FILTER_ACCEPT : NodeFilter.FILTER_REJECT;
      }
    });
    let n; while ((n = tw.nextNode())) applyTextNode(n);
    root.querySelectorAll('[title],[placeholder]').forEach(applyAttrs);
  }

  function translateAll() { walk(document.body); }

  function restoreAll() {
    document.querySelectorAll('[title],[placeholder]').forEach(el => {
      const st = origAttr.get(el); if (!st) return;
      for (const a in st) if (st[a] != null) el.setAttribute(a, st[a]);
    });
    const tw = document.createTreeWalker(document.body, NodeFilter.SHOW_TEXT);
    let n; while ((n = tw.nextNode())) if (orig.has(n)) n.nodeValue = orig.get(n);
  }

  const obs = new MutationObserver(muts => {
    if (lang === 'kr') return;
    for (const m of muts) {
      if (m.type === 'characterData') { applyTextNode(m.target); continue; }
      m.addedNodes.forEach(node => {
        if (node.nodeType === Node.TEXT_NODE) applyTextNode(node);
        else if (node.nodeType === Node.ELEMENT_NODE) walk(node);
      });
    }
  });

  /* ${argN} → 값 (woofia_sim/effects.py resolve_placeholders 복제) */
  function resolveDesc(desc, params) {
    return (desc || '').replace(/\$\{(\w+)\}/g, (m, k) => {
      const v = params ? params[k] : undefined;
      return (v === undefined || v === null) ? m : String(v);
    });
  }

  /* 이름 + 스킬설명(해석본) 로드 → 이름은 SUB, 설명은 EXACT(노드 전체 일치) */
  async function loadNames(code) {
    const R = RES[code];
    if (!R || R.names.length) { if (R && !R._sub) buildSub(R); return; }
    const nf = R.nameField, df = nf.replace('name_', 'desc_');
    const pairs = [];
    try {
      const chars = await fetch('data/chars.json', { cache: 'force-cache' }).then(r => r.json());
      (Array.isArray(chars) ? chars : Object.values(chars)).forEach(c => {
        if (c && c.name_kr && c[nf]) pairs.push([c.name_kr.trim(), c[nf]]);
      });
    } catch (e) { /* names stay Korean */ }
    try {
      const skills = await fetch('data/skills.json', { cache: 'force-cache' }).then(r => r.json());
      Object.values(skills).forEach(slots => Object.values(slots).forEach(sd => {
        if (!sd) return;
        if (sd.name_kr && sd[nf]) pairs.push([sd.name_kr.trim(), sd[nf]]);
        Object.values(sd.levels || {}).forEach(e => {
          if (!e || !e.desc_kr || !e[df]) return;
          // 브라우저가 innerHTML 삽입 시 \r\n→\n 정규화하므로 키도 \n 으로 맞춘다(멀티라인 설명 매칭).
          const nl = s => s.replace(/\r\n?/g, '\n');
          const rk = nl(resolveDesc(e.desc_kr, e.params).trim());
          const ren = nl(resolveDesc(e[df], e.params).trim());
          if (rk && rk !== ren && HANGUL.test(rk) && R.EXACT[rk] == null) R.EXACT[rk] = ren;
        });
      }));
    } catch (e) { /* skip */ }
    const seen = new Set();
    R.names = pairs.filter(([k]) => (seen.has(k) ? false : seen.add(k)));
    // 이름은 노드 전체 일치(EXACT)로도 등록 — 1글자 이름(멍/란)도 슬롯·랭킹·드롭다운에서 번역됨
    R.names.forEach(([k, v]) => { if (R.EXACT[k] == null) R.EXACT[k] = v; });
    buildSub(R);
  }

  /* ── 언어 전환 ── */
  let menuOpen = false;
  async function setLang(next) {
    if (!codes.includes(next) || next === lang) { closeMenu(); return; }
    const prev = lang;
    lang = next;
    localStorage.setItem(LS_KEY, lang);
    if (lang !== 'kr') { await loadNames(lang); if (prev !== 'kr') restoreAll(); translateAll(); }
    else restoreAll();
    syncMenu(); closeMenu();
  }

  /* ── UI: "Language" 버튼 + 선택 메뉴 (좌측 하단) ── */
