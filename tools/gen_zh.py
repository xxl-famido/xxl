def emit(keys: list[str], override: dict) -> str:
    lines, missing = [], []
    for k in keys:
        v = override.get(k, CN.get(k))
        if v is None:
            missing.append(k)
            v = k  # fallback: leave Korean (visible -> easy to spot)
        lines.append(f"        {json.dumps(k, ensure_ascii=False)}: {json.dumps(v, ensure_ascii=False)},")
    if missing:
        Path(__file__).resolve().parent.joinpath("missing.txt").open("a", encoding="utf-8").write("\n".join(missing) + "\n")
        print(f"MISSING: {len(missing)} (see tools/missing.txt)")
    return "\n".join(lines)


