def _block(src: str, name: str) -> str:
    m = re.search(r"\b" + name + r":\s*\{", src)
    i = m.end()
    depth = 1
    while depth:
        if src[i] == "{":
            depth += 1
        elif src[i] == "}":
            depth -= 1
        i += 1
    return src[m.end():i - 1]


def _keys(blk: str) -> list[str]:
    # 주석 제거(주석 뒤 첫 키 누락 방지) 후, "키": 형태를 전부 매칭
    blk = re.sub(r"/\*.*?\*/", "", blk, flags=re.S)
    blk = re.sub(r"(^|[^:])//[^\n]*", r"\1", blk)
    out, seen = [], set()
    for m in re.finditer(r"""(['"])((?:\\.|(?!\1).)*?)\1\s*:""", blk):
        k = m.group(2)
        if k not in seen:
            seen.add(k)
            out.append(k)
    return out


